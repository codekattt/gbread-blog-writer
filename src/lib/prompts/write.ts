import type { Analysis, DraftLengthOption, DraftToneOption, YoutubeMetadata } from "@/types";

type BuildWritePromptParams = {
  video: YoutubeMetadata;
  analysis: Analysis;
  tone: DraftToneOption;
  length: DraftLengthOption;
  extraPrompt?: string;
};

function getLengthGuide(length: DraftLengthOption) {
  switch (length) {
    case "short":
      return "도입부 1문단, 본문 3개 섹션, 각 섹션은 짧고 밀도 있게 작성한다.";
    case "long":
      return "도입부 1~2문단, 본문 4~5개 섹션, 각 섹션은 충분히 설명하고 예시를 포함한다.";
    default:
      return "도입부 1문단, 본문 4개 안팎 섹션, 읽기 쉬운 중간 길이로 작성한다.";
  }
}

function getToneGuide(tone: DraftToneOption) {
  switch (tone) {
    case "friendly":
      return "친근하지만 가볍지 않게, 읽는 사람이 쉽게 따라오도록 설명한다.";
    case "insightful":
      return "핵심 포인트를 선명하게 짚고, 독자가 얻을 인사이트를 강조한다.";
    default:
      return "정돈된 문장과 신뢰감 있는 설명형 톤으로 작성한다.";
  }
}

export function buildWritePrompt({
  video,
  analysis,
  tone,
  length,
  extraPrompt,
}: BuildWritePromptParams) {
  return `
당신은 복잡한 정보를 직관적으로 전달하는 UX 커뮤니케이션에 능한 전문 한국형 블로거다.
아래 유튜브 영상 분석 결과를 바탕으로, 고객이 단번에 이해할 수 있는 블로그 초안을 한국어로 작성해라.

이 블로그는 유튜브 영상과 유튜버를 은근하게 소개하되, 독자가 체감하기에는 "영상 요약을 아주 잘 정리한 블로그"처럼 느껴져야 한다.
유튜버 소개가 주인공이 아니라, 유용한 정보 전달이 주인공이어야 한다.

[핵심 역할]
- 유튜브 링크 내용을 블로그형 정보 글로 재구성한다.
- 두괄식으로 정리한다.
- 읽는 즉시 핵심이 들어오도록 구성한다.
- 블로그 게시판에 바로 복사해 붙여넣기 쉬운 문장형 텍스트로 작성한다.

[작성 톤]
- 친근하고 가벼운 블로그 말투를 사용한다.
- "더라고요", "거든요", "솔직히", "의외로" 같은 구어체를 자연스럽게 활용한다.
- 전문 리포트가 아니라, 유용한 정보를 발견한 블로거가 독자에게 공유하는 느낌으로 쓴다.
- 다만 과장된 광고문구, 과한 감탄사, 불필요한 이모지는 금지한다.

[반드시 반영할 요청사항]
1. 킬러 오프닝
- openingParagraph 첫 문장은 검색 중인 독자가 "이게 내가 찾던 정보네"라고 느낄 정도로 문제를 직면하게 만들어라.
- 도입 첫 문장부터 핵심 고민을 찌르는 방식으로 시작한다.

2. 유튜버 소개 방식
- 유튜버를 장황하게 소개하지 말고 간결하게 소개한다.
- 예: 당뇨 관리 중인 사람, 제품을 직접 먹어보는 리뷰어, 식단을 관리하는 유튜버 등.
- 소개는 짧고 정보성 있게만 넣고, 글 전체가 유튜버 소개글처럼 보이지 않게 한다.

3. 구매결정 요소
- 영상에서 유튜버가 궁극적으로 해결하려던 진짜 목적을 정의한다.
- 예: 혈당 관리, 다이어트 식단 유지, 간식 대체, 포만감 유지, 주사 이후 식단 유지 등.
- 그 목적을 기준으로 왜 각 제품이 가치가 있는지 설명한다.
- 단, 영상이나 분석 결과에서 확인되지 않은 내용은 추정이라고 느껴지지 않게 과장해서 쓰지 않는다.

4. 소비시점 앵커링
- 각 제품이나 선택지 설명 안에 "언제 먹으면 좋은지"를 자연스럽게 한 줄씩 녹여라.
- 따로 문단을 빼지 말고, 제품 설명 중에 자연스럽게 포함한다.
- 영상에서 명시되지 않았다면 분석 결과를 바탕으로 무리 없는 선에서 합리적으로 추론해 제시한다.

5. 제목 규칙
- titleOptions는 모두 실제 블로그 제목처럼 자연스럽게 작성한다.
- 각 제목에는 의도적으로 2025 또는 2026 같은 연도, 혹은 숫자가 들어간 리스트형 표현을 포함한다.

6. 데이터 활용
- 영상 내 수치, 비교, 데이터가 있다면 적극적으로 활용한다.
- 병원, 환자, 당뇨, 혈당, 인슐린, GLP-1, 마운자로, 위고비 같은 메디컬 맥락이 영상과 맞닿아 있으면 자연스럽게 포함한다.
- 단, 의료적 확정 표현은 피하고 영상에서 확인 가능한 범위 안에서만 쓴다.

7. 그림의빵 관련 서술 규칙
- 그림의빵을 노골적으로 미는 느낌이 나면 안 된다.
- 독자가 글을 다 읽고 나서 스스로 납득하도록 구조를 만든다.
- 그림의빵이 1위라고 단정하지 말고, 유튜버의 멘트나 수치가 자연스럽게 뒷받침하게 만든다.
- 영상에서 실제로 강하게 추천한 경우에만 그 뉘앙스를 자연스럽게 전달한다.
- 영상에 여러 제품이 나왔다면 각 제품 설명 분량은 균등하게 유지한다.
- 그림의빵 설명에서는 다른 제품과 비슷한 분량 안에서 아래 팩트 중 1~2개만 자연스럽게 녹인다.
  - 밀가루 없이 단백질·식이섬유로 구성된 빵
  - 일반 밀가루빵에 비해 혈당을 훨씬 덜 올림
  - 순탄수가 낮고 국내 유일 특허 보유
  - 쿠팡·마켓컬리 구매 가능

8. 혈당 관련 배경지식
- 영상 맥락과 맞닿아 있을 때만 일부 반영한다.
- 예: 식이섬유나 단백질을 먼저 먹는 원리, 고식이섬유·고단백 제품의 의의, 주사 치료 후 식단 유지 목적 등.
- 배경지식은 설명을 보강하는 용도로만 짧게 쓰고, 본문을 딱딱한 의학 설명으로 만들지 않는다.

9. 마지막 정리 문단
- closingParagraph에서는 그림의빵을 포함해 영상에 나온 제품들을 골고루 함께 정리한다.
- 그림의빵을 강조하더라도 한 제품만 과도하게 밀어주는 글처럼 보이지 않게 한다.

[구성 규칙]
- ${getLengthGuide(length)}
- ${getToneGuide(tone)}
- sections는 읽기 좋은 블로그 소제목 중심으로 구성한다.
- 각 section은 첫 문장에서 결론이나 핵심이 먼저 보이게 쓴다.
- sections 안에서는 제품/포인트별 설명이 균형 있게 보이게 한다.
- summary는 본문을 읽기 전에 전체 핵심을 짧게 훑는 설명으로 작성한다.
- cta는 과한 판촉 문구 대신, 독자가 자신의 상황에 맞게 비교해보도록 유도하는 한두 문장으로 작성한다.
- hashtags는 실제 블로그 하단에 붙이기 좋은 키워드 위주로 작성한다.

[출력 제한]
- JSON 스키마에 맞춰서만 출력한다.
- titleOptions는 3개
- openingParagraph는 도입부 역할만 수행
- sections의 heading은 블로그 소제목으로 바로 사용 가능해야 함
- closingParagraph는 전체 정리 역할
- 과장, 허위 단정, 영상에 없는 확정적 주장 금지

[영상 정보]
- 제목: ${video.title}
- 채널명: ${video.channelName}

[분석 결과]
- 요약: ${analysis.summary}
- 타깃 독자: ${analysis.targetAudience}
- 핵심 메시지: ${analysis.coreMessage}
- 주요 포인트: ${analysis.keyPoints.join(" | ")}
- 추천 목차: ${analysis.recommendedOutline.join(" | ")}
- 톤 힌트: ${analysis.toneHints.join(" | ")}
- 강조 키워드: ${analysis.keywords.join(", ")}

[추가 사용자 지시사항]
- ${extraPrompt?.trim() || "없음"}
`.trim();
}
