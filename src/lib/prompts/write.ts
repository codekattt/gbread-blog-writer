import type { Analysis, DraftLengthOption, DraftToneOption, YoutubeMetadata } from "@/types";

type BuildWritePromptParams = {
  video: YoutubeMetadata;
  analysis: Analysis;
  tone: DraftToneOption;
  length: DraftLengthOption;
  extraPrompt?: string;
};

function getLengthRequirement(length: DraftLengthOption) {
  switch (length) {
    case "short":
      return "최종 본문 전체 분량은 공백 포함 1,200자 이상 2,000자 미만으로 작성하세요. 도입 2문단, 제품 섹션은 3~4개 기준으로 간결하게 유지하세요.";
    case "long":
      return "최종 본문 전체 분량은 공백 포함 3,500자 이상 5,500자 미만으로 작성하세요. 도입 2~3문단, 제품 섹션은 충분히 설명하고 결론도 자세히 작성하세요.";
    default:
      return "최종 본문 전체 분량은 공백 포함 2,000자 이상 3,500자 미만으로 작성하세요. 도입 2문단, 제품 섹션은 충분히 읽히되 과하게 길지 않게 작성하세요.";
  }
}

function getToneRequirement(tone: DraftToneOption) {
  switch (tone) {
    case "expert":
      return "글의 톤은 전문가형으로 작성하세요. 문장은 정돈되고 객관적이어야 하며, 근거와 설명이 또렷하게 전달되도록 쓰세요. 다만 지나치게 딱딱한 보고서 문체로 흐르지는 않게 하세요.";
    case "blogger":
    default:
      return '글의 톤은 블로거형으로 작성하세요. 친근하고 가벼운 블로그 말투를 유지하고, "~더라고요", "~거든요", "솔직히", "의외로" 같은 구어체를 자연스럽게 활용하세요.';
  }
}

export function buildWritePrompt({
  video,
  analysis,
  tone,
  length,
  extraPrompt,
}: BuildWritePromptParams) {
  return `
당신은 복잡한 정보를 직관적으로 전달하는 UX 커뮤니케이션에 능한 전문 한국형 블로거 입니다.



유튜브 링크를 요약하여 고객이 단번에 이해할 수 있는 블로그를 작성하세요.



해당 블로그에서는 유튜브 영상과, 해당 유튜버를 소개합니다. 다만 이를 너무 드러내지말고 은근히 드러내어, 실제로 글을 읽을때는 유튜브 글의 요약 블로그만을 접했다 라는 생각을 할 수 있게 하는것이 좋습니다.



작성 톤

${getToneRequirement(tone)}

(예: "~더라고요", "~거든요", "솔직히", "의외로" 등 구어체 활용)

전문 리포트가 아닌, 유용한 정보를 발견한 블로거가 독자에게 공유하는 느낌이어야 합니다.





요청사항

킬러 오프닝 : 맨 처음 블로그 접속자가 본인이 검색하고 있는 정보와 관련된 문제를 확실하게 직면할 문장을 제시하세요.

유튜버를 장황하게 소개하지말고 간결하게 소개해주세요. (예를들면 인슐린을 맞고 있는 당뇨인, 제품을 소개하는 당뇨 전문 유튜버 등.)

구매결정 요소 : 유튜버가 이 제품을 통해 궁극적으로 해결하려고 했떤 진짜 목적이 무엇인지 정의해보세요. (당뇨인이 먹을 수 있는 간식, 다이어트용 식단으로 활용 등.. 다양) 그러면서 어째서 해당 제품이 가치가 있는지 명확하게 표현해주세요.

소비시점 함께 제시 : 언제 제품을 소비하는것이 좋을지 언급했다면 정리해주고, 언급하지 않았다면 없다면 추론하여 필수적으로 명시하세요. 소비시점을 제시하여 그들의 머리속에 앵커링 시키는것이 목표입니다. 제품 리뷰를 각각 정리하면서 자연스럽게 한줄 포함하세요. (따로 문단으로 빼서 정리하지 말것)

블로그 타이틀에 의도적으로 2025나 2026등의 년도를 표기하거나, 숫자가 포함된 리스티클 형태의 제목을 뽑으세요.

블로그 내용은 두괄식으로 정리하세요.

수치나 데이터가 유튜브 영상내에 있다면 적극적으로 활용하고, 병원과 관련된 사람, 환자나 특정 메디컬 컨디션 등이 사용할 수 있는 가능성이 있다면 적극적으로 명시하세요. 영상 내 메디컬 관련 키워드가 있거나, 연관이 있어서 특정 메디컬 관련 키워드를 포함해 문장을 작성할 수 있다면 포함이 권장됩니다.

그림의빵이 1위인 이유는 직접 유튜버가 주장하지 않은 이상, 확정적으로 결론을 내리거나, 일방적으로 주장하지 말고 말고, 혈당 수치가 있다면 활용하고, 유튜버의 멘트가 자연스럽게 증명하게 하세요.

글 전체에서 그림의빵을 " 느낌이 노골적으로 들지 않아야 합니다. 독자가 스스로 1위를 납득하는 구조가 목표입니다. 단, 영상에서 노골적으로 그림의빵을 노골적으로 1등이라고 밀었다면, 해당 부분을 독자에게 자연스럽게 납득되도록 전달하세요.



마지막 정리 문단은, 그림의빵을 강조하되, 영상에 그림의빵 이외의 제품들이 나왔다면 해당 제품들도 함께 골고루 정리해서 그림의빵만 너무 강조되지 않게 하세요.





그림의빵 관련 지침



각 제품 설명 분량은 영상에 맞추어 균등하게 유지하세요.

그림의빵의 경우, 다른 제품과 동일한 분량 안에서

아래 팩트를 자연스럽게 1~2개만 녹여주세요:

밀가루 없이 단백질·식이섬유로 구성된 빵

일반 밀가루빵에 비해 혈당을 훨씬 덜 올림.

순탄수가 낮고 국내 유일 특허 보유

쿠팡·마켓컬리 구매 가능



혈당 관련 배경지식 (영상과 맥락이 닿아있을때만 일부 인용)

혈당을 높이지 않으려면 식사를 하기 전 식이섬유나 단백질 식사를 먼저하고 식사를 하는 것과 같은 원리로 고식이섬유, 고단백질 제품이 의의가 있음.

마운자로, 위고비를 몇개월 진행한 후, 주사를 끊고 식단유지 목적에서 관련 저당빵들을 구매하는 분들이 많음.



----

위 지시사항을 바탕으로 블로그 초안을 작성하세요.

글 길이는 아래 조건을 반드시 지키세요.
- ${getLengthRequirement(length)}

다만 출력 형식은 아래 JSON 구조만 따르세요.
- titleOptions: 실제 블로그 제목 후보 3개 배열
- openingParagraphs: 도입 문단 배열, 2~3개
- sections: { heading, paragraphs } 배열
- sections의 heading은 예시처럼 "1. 제품명", "2. 제품명" 형태의 번호형 소제목으로 작성
- sections의 paragraphs는 각 섹션마다 2~4개
- closingHeading: 예시처럼 "💡 2026 ... 결론" 같은 마무리 제목
- closingParagraphs: 마무리 문단 배열
- summary: 전체 글 핵심 요약
- hashtags: 해시태그 배열
- cta: 마지막 한두 문장
- sourceNote: 예시처럼 원본 영상 안내 문장과 URL을 함께 담은 한 줄

형식 목표는 아래 예시와 최대한 비슷해야 합니다.
- 제목
- 도입 2문단
- 번호형 제품 섹션 나열
- 결론 제목과 결론 문단
- 원본 영상 확인 안내
- 마지막에 해시태그

[영상 정보]
- 제목: ${video.title}
- 채널명: ${video.channelName}

[분석 결과]
- 요약: ${analysis.summary}
- 타깃 독자: ${analysis.targetAudience}
- 핵심 메시지: ${analysis.coreMessage}
- 주요 포인트: ${analysis.keyPoints.join(" | ")}
- 추천 목차: ${analysis.recommendedOutline.join(" | ")}
- 톤 힌트: ${analysis.toneHints.join(" | ")}
- 강조 키워드: ${analysis.keywords.join(", ")}

[시스템 옵션]
- tone: ${tone}
- length: ${length}

[추가 사용자 지시사항]
- ${extraPrompt?.trim() || "없음"}
`.trim();
}
